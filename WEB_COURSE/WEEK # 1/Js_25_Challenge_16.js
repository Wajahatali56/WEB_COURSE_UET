function basicop(operation, val1 , val2){
    let result;
    switch(operation){
        case '+':
            result = val1 + val2 ; 
            break;
            case '-':
                result = val1 - val2 ; 
                break;
                case '*':
                    result = val1 * val2 ; 
                    break;
                    case '/':
                        result = val1 / val2 ; 
                        break;
    }
    return result;
}


















